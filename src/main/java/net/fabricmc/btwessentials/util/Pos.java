package net.fabricmc.btwessentials.util;

public class Pos {
    public double x;
    public double y;
    public double z;
    public int dimension;

    public Pos(double x, double y, double z, int dimension) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.dimension = dimension;
    }
}
